
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2";
import { GoogleGenerativeAI } from "https://esm.sh/@google/generative-ai@0.1.3";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }
  
  // Create Supabase client
  const supabaseClient = createClient(
    Deno.env.get('SUPABASE_URL') ?? '',
    Deno.env.get('SUPABASE_ANON_KEY') ?? '',
    {
      global: {
        headers: { Authorization: req.headers.get('Authorization')! },
      },
    }
  );
  
  let imageId = null;
  
  try {
    // Parse request body as text to avoid recursion issues
    const requestText = await req.text();
    console.log('Raw request body:', requestText);
    
    try {
      const requestData = JSON.parse(requestText);
      imageId = requestData.imageId;
    } catch (parseError) {
      console.error('Error parsing request JSON:', parseError);
      throw new Error('Invalid JSON in request body');
    }
    
    if (!imageId) {
      throw new Error('Image ID is required');
    }
    
    console.log('Processing image with ID:', imageId);
    
    // Get image data
    const { data: imageData, error: imageError } = await supabaseClient
      .from('user_images')
      .select('*')
      .eq('id', imageId)
      .maybeSingle();
    
    if (imageError) {
      console.error('Error fetching image:', imageError);
      throw new Error(`Error fetching image: ${imageError.message}`);
    }
    
    if (!imageData) {
      console.error('Image not found:', imageId);
      return new Response(
        JSON.stringify({ error: 'Image not found' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 404 }
      );
    }

    console.log('Found image to process:', imageData.original_image_path);
    
    // Update status to processing
    const { error: updateError } = await supabaseClient
      .from('user_images')
      .update({
        status: 'processing',
        updated_at: new Date().toISOString(),
      })
      .eq('id', imageId);
      
    if (updateError) {
      console.error('Error updating image status:', updateError);
      throw new Error(`Error updating image status: ${updateError.message}`);
    }

    // Get the original image
    const imageResponse = await fetch(imageData.original_image_path);
    if (!imageResponse.ok) {
      throw new Error(`Failed to fetch original image: ${imageResponse.status} ${imageResponse.statusText}`);
    }
    
    // Get image data as bytes for Gemini
    const imageBytes = await imageResponse.arrayBuffer();
    const base64Image = btoa(String.fromCharCode(...new Uint8Array(imageBytes)));
    const mimeType = imageResponse.headers.get('content-type') || 'image/jpeg';
    
    // Process with Gemini using official SDK
    const geminiApiKey = Deno.env.get('GEMINI_API_KEY');
    if (!geminiApiKey) {
      throw new Error('Gemini API key not configured');
    }
    
    console.log('Initializing Google Generative AI with API key');
    
    // Initialize the Google Generative AI SDK
    const genAI = new GoogleGenerativeAI(geminiApiKey);
    
    // Use the image generation model
    const model = genAI.getGenerativeModel({
      model: "gemini-1.5-flash-latest",
    });
    
    console.log('Sending request to Gemini API using SDK');
    
    // Create image part
    const imagePart = {
      inlineData: {
        data: base64Image,
        mimeType: mimeType
      }
    };
    
    // The text prompt
    const textPart = "Transform this photo into a Studio Ghibli style cartoon.";
    
    // Generate content with the model
    const result = await model.generateContent([textPart, imagePart]);
    const response = await result.response;
    
    console.log('Received response from Gemini API');
    
    // Find the image in the parts
    if (!response.candidates || response.candidates.length === 0) {
      console.error('No candidates in Gemini response');
      throw new Error('No image was generated by Gemini');
    }
    
    const parts = response.candidates[0]?.content?.parts;
    if (!parts || parts.length === 0) {
      console.error('No parts in Gemini response');
      throw new Error('No image was generated by Gemini');
    }
    
    const generatedImage = parts.find(
      part => part.inlineData && part.inlineData.mimeType && part.inlineData.mimeType.startsWith('image/')
    );
    
    if (!generatedImage || !generatedImage.inlineData || !generatedImage.inlineData.data) {
      console.error('No image found in response parts');
      throw new Error('No image was generated by Gemini');
    }
    
    const toonifiedImage = generatedImage.inlineData.data;
    const generatedMimeType = generatedImage.inlineData.mimeType;
    
    console.log('Successfully extracted toonified image');
    
    // Upload the generated image to storage
    const fileName = `toonified-${imageId}.${generatedMimeType.split('/')[1] || 'jpg'}`;
    const filePath = `toonified/${fileName}`;
    
    // Convert base64 back to binary for storage
    const binaryData = Uint8Array.from(atob(toonifiedImage), c => c.charCodeAt(0));
    
    // Upload to storage
    const { error: uploadError } = await supabaseClient.storage
      .from('images')
      .upload(filePath, binaryData, {
        contentType: generatedMimeType,
        upsert: true
      });
    
    if (uploadError) {
      console.error('Error uploading toonified image:', uploadError);
      throw uploadError;
    }
    
    // Get public URL
    const { data: urlData } = supabaseClient.storage
      .from('images')
      .getPublicUrl(filePath);
    
    const toonifiedImageUrl = urlData.publicUrl;
    
    // Update the record with the processed image
    const { error: finalUpdateError } = await supabaseClient
      .from('user_images')
      .update({
        toonified_image_path: toonifiedImageUrl,
        status: 'complete',
        updated_at: new Date().toISOString(),
      })
      .eq('id', imageId);
      
    if (finalUpdateError) {
      console.error('Error updating image record:', finalUpdateError);
      throw new Error(`Error updating image record: ${finalUpdateError.message}`);
    }
    
    console.log('Successfully processed and stored toonified image:', toonifiedImageUrl);
    
    return new Response(
      JSON.stringify({ success: true, imageUrl: toonifiedImageUrl }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 200 }
    );
  } catch (error) {
    // Log the full error for debugging
    console.error('Error processing image:', error);
    
    // Try to update the image status to error if we have an imageId
    if (imageId) {
      try {
        await supabaseClient
          .from('user_images')
          .update({
            status: 'error',
            updated_at: new Date().toISOString(),
          })
          .eq('id', imageId);
        console.log('Updated image status to error');
      } catch (updateError) {
        console.error('Error updating image status after failure:', updateError);
      }
    }
    
    // Return a friendly error message
    return new Response(
      JSON.stringify({ 
        error: error instanceof Error ? error.message : 'Unknown error occurred',
        details: error instanceof Error ? error.stack : undefined 
      }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 500 }
    );
  }
});
